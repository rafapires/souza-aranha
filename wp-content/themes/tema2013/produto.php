			<h1>Plataforma de CRM</h1>
			<div class="well">
				<h2 class="label label-sucess">Benefício principal:</h2>
				<p>Permite a visão 360° do cliente, por meio da integração das informações das áreas de Vendas, Marketing e Serviços a Clientes em uma única base de dados. A Plataforma de CRM permite o aperfeiçoamento e a informatização dos processos de vendas e de serviços a clientes. Fortalece a infraestrutura de relacionamento com os clientes a menores custos.</p>
			</div>

			<p>Contrariamente a crença popular CRM não é software. A essência do CRM é colocar o cliente no centro de suas estratégias para tornar a marca mais competitiva. Seu fundamento parte do conhecimento profundo do relacionamento do cliente com a marca, isto é do seu histórico de compra ao seu nível de satisfação para conquistar e fidelizar os clientes com total sucesso.</p>
			<div style="-webkit-border-radius: 5px; -webkit-border-bottom-right-radius: 20px; -moz-border-radius: 5px; -moz-border-radius-bottomright: 20px; border-radius: 5px; border-bottom-right-radius: 20px; width: 195px; height: auto; background-color: #fff; box-shadow: 5px 5px 14px #ccc; -moz-box-shadow: 5px 5px 14px #ccc; -webkit-box-shadow: 5px 5px 14px #ccc; margin-left: 15px; padding-top: 10px; padding-left: 10px; padding-bottom: 10px; padding-right: 100px; float: right; letter-spacing: -1px; color: #2E9292; font-weight: bold; font-size: 1.2em; font-family: Arial, Helvetica, sans-serif; line-height: 1.3em; clear: right;">
				A plataforma, baseada na metodologia e-spider® desenvolvida com exclusividade pela Souza Aranha, está apoiada em cinco pilares: os Canais de Relacionamento, o Gestor de Campanhas, os Processos de Relacionamento, o Sistema de Informação e as Ferramentas de CRM.
			</div>
			<p>Diversos programas de relacionamento falham apesar de fundamentados em estratégias consistentes e bancos de dados bem formatados, por falta de uma infraestrutura adequada. A solução encontrada pela Souza Aranha para garantir os melhores resultados é a Plataforma de CRM, que integra todos os recursos necessários ao pleno funcionamento da estratégia de relacionamento.</p>
			<p>A plataforma, baseada na metodologia<strong> e-spider®</strong> desenvolvida com exclusividade pela Souza Aranha, está apoiada em cinco pilares: os <strong>Canais de Relacionamento</strong>, o <strong>Gestor de Campanhas</strong>, os <strong>Processos de Relacionamento</strong>, o <strong>Sistema de Informação</strong> e as <strong>Ferramentas de CRM</strong>.</p>
			<ul style="color: #737B5E; line-height: 150%;">
				<span style="color: #009999; font-size: 1.3em; letter-spacing: -1px; font-weight: bold; font-family: Arial, Helvetica, sans-serif">Canais de Relacionamento:</span>
				<div style="color: #737B5E; line-height: 180%;  margin-top: 10px; margin-bottom: 25px">
					são otimizados pela plataforma por meio da qualidade de sua interface – interativa com a web, call center, SMS, telefonia móvel – e de suas ferramentas de disparo, elevando a capacidade de atingir com rapidez e precisão o target desejado. Estes potenciais clientes são levados a se relacionar ativamente com a marca, além de enriquecer o banco de dados a cada interação.
				</div>
				<div style="-webkit-border-top-left-radius: 8px; -webkit-border-bottom-left-radius: 8px; -moz-border-radius-topleft: 8px; -moz-border-radius-bottomleft: 8px; border-top-left-radius: 8px; border-bottom-left-radius: 8px; background-color: #fff; box-shadow: 5px 5px 14px #ccc; -moz-box-shadow: 5px 5px 14px #ccc; -webkit-box-shadow: 5px 5px 14px #ccc; margin-top: 20px; margin-left: 20px; margin-bottom: 30px; padding: 14px; border-top: solid #000035 3px; border-bottom: solid #ebebeb 1px; border-right: solid #ebebeb 1px; border-left: solid #ebebeb 1px; float: right; width: 210px; height: auto">
					<div>
						<p><span style="color: #000035; font-size: 1.2em; text-transform: uppercase; letter-spacing: -1px; font-weight: bold;">Banco de Dados </span></p>
						<p><span style=" text-transform: none; font-weight: normal; letter-spacing: normal; clear: both; text-align: left; padding-top: 12px; color: #006F77; font-size: 1.1em; font-family: Times, serif, Georgia;">para a implantação de uma plataforma de CRM, primeiramente a Souza Aranha efetua uma avaliação completa da estrutura e qualidade das bases de dados dos clientes e  alimentadores on-line e offline  da empresa, com foco em qualidade e pertinência. Após isso a Souza Aranha desenha a arquitetura do banco de dados para atender aos objetivos das estratégias de relacionamento.</span></p>
						<p><span style=" text-transform: none; font-weight: normal; letter-spacing: normal; clear: both; text-align: left; padding-top: 12px; color: #006F77; font-size: 1.1em; font-family: Times, serif, Georgia;">Em seguida supervisiona os processos de tratamento de dados (data cleansing) e recomenda fontes de primeira linha para o enriquecimento dos dados.</span></p>
						<p><span style=" text-transform: none; font-weight: normal; letter-spacing: normal; clear: both; text-align: left; padding-top: 12px; color: #006F77; font-size: 1.1em; font-family: Times, serif, Georgia;">E finalmente desenvolve os manuais completos para a gestão operacional do banco de dados de clientes, treinando as equipes envolvidas.</span> </p>
					</div>
				</div>
				<p><span style="color: #333333; font-size: 1.3em; letter-spacing: -1px; font-weight: bold; font-family: Arial, Helvetica, sans-serif">Gestor de Campanhas: </span></p>
				<div style="color: #737B5E; line-height: 180%;  margin-top: 10px; margin-bottom: 25px">
					<p>a ferramenta dos sonhos dos profissionais de marketing, mais uma forte funcionalidade da Plataforma de CRM. Baseada na metodologia <strong>e-spider®</strong>, trabalha por meio de um amplo conjunto de filtros que proporcionam a segmentação ideal do target para cada campanha,  em função do comportamento das respostas, e disparando automaticamente novas ações customizadas para estes segmentos, até se atingir o resultado desejado.</p>
					<p>Oferece uma grande facilidade à execução de campanhas de prospecção, ativação e retenção e eventos por meio dos canais de relacionamento. Potencializa o uso de ferramentas como Google AdWords e integração às redes sociais, como o monitoramento do Twitter e a incorporação do Facebook à plataforma..</p>
				</div>
				<div>
					<a href="http://www.souza-aranha.com.br/portal/wp-content/uploads/2010/12/Plataforma-de-CRM-Front-End3.jpg"><img src="http://www.souza-aranha.com.br/portal/wp-content/uploads/2010/12/Plataforma-de-CRM-Front-End3.jpg" alt="" width="450" height="256" border="0" class="aligncenter size-full wp-image-768" title="Plataforma de CRM Front End"></a>
				</div>
				<p><span style="color: #009999; font-size: 1.3em; letter-spacing: -1px; font-weight: bold; font-family: Arial, Helvetica, sans-serif">Processos de Relacionamento:</span></p>
				<div style="color: #737B5E; line-height: 180%;  margin-top: 10px; margin-bottom: 25px">
					<p>racionalizar e automatizar os processos de relacionamento permite reduzir sensivelmente as atividades operacionais das equipes de Vendas, Serviços a Clientes e Marketing. Assim, as equipes envolvidas podem dedicar mais tempo aos clientes e a otimização dos resultados. Um bom exemplo é a automação das atividades das Réguas de Relacionamento: há um ganho na qualidade de relacionamento e um melhor acompanhamento dos resultados do desempenho da régua.</p>
					<p>A automação dos processos de CRM pode, por exemplo, ajudar a área de Vendas a elevar o desempenho de seus profissionais ou permitir uma consistente solução para as demandas recebidas em Serviços a Clientes, resultando em maior taxa de fechamento de vendas e fidelização de clientes.</p>
				</div>
				<div>
					<a href="http://www.souza-aranha.com.br/portal/wp-content/uploads/2010/12/Plataforma-de-CRM-Back-End1.jpg"><img src="http://www.souza-aranha.com.br/portal/wp-content/uploads/2010/12/Plataforma-de-CRM-Back-End1.jpg" alt="" width="450" height="229" border="0" class="aligncenter size-full wp-image-769" title="Plataforma de CRM Back End"></a>
				</div>
				<p><span style="color: #333333; font-size: 1.3em; letter-spacing: -1px; font-weight: bold; font-family: Arial, Helvetica, sans-serif">Sistema de Informação: </span></p>
				<div style="color: #737B5E; line-height: 180%;  margin-top: 10px; margin-bottom: 25px">
					sua arquitetura, baseada na metodologia <strong>e-spider®</strong>, permite integrar as informações sobre o cliente existentes em Vendas, Marketing e Serviços a Clientes em uma única base de dados, permitindo obter a Visão Única do Cliente, e também concilia a qualidade da gestão do banco de dados à alta capacidade analítica. Além disso, oferece elevada facilidade e rapidez na criação de relatórios e painéis (dashboards) para a gestão do cliente nas variadas áreas da empresa e para a avaliação dos resultados.
				</div>
				<p><span style="color: #009999; font-size: 1.3em; letter-spacing: -1px; font-weight: bold; font-family: Arial, Helvetica, sans-serif">Ferramentas: </span></p>
				<div style="color: #737B5E; line-height: 180%;  margin-top: 10px; margin-bottom: 25px">
					a Plataforma de Relacionamento Souza Aranha é amplamente customizável e baseada no Salesforce.com, líder mundial de CRM na era da computação nas nuvens. O prazo para sua implantação oscila de 60 a 90 dias e o investimento inicial é menor em relação aos seus concorrentes. Como resultado, o payback é alcançado em até quatro a seis meses após a sua implantação, em média. A solução utilizada de computação nas nuvens proporciona à empresa focar em seu negócio, sem preocupações com infraestrutura, manutenção ou melhorias. Isto ela recebe automaticamente, sem a necessidade de recustomizar as novas versões.
